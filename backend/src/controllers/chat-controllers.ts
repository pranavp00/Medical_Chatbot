import { spawn } from 'child_process';
export const generateChatCompletion = async (
  req: Request,
  res: Response,
  next: NextFunction
) => {
  const { message } = req.body;
  try {
    const user = await User.findById(res.locals.jwtData.id);
    if (!user)
      return res
        .status(401)
        .json({ message: "User not registered OR Token malfunctioned" });
    // grab chats of user
    const chats = user.chats.map(({ role, content }) => ({
      role,
      content,
    }));
    chats.push({ content: message, role: "user" });
    user.chats.push({ content: message, role: "user" });

    // Call Python script
    const pythonScript = 'llm.py';
    const dynamicValue = chats.map(({ content }) => content).join('\n');
    const pythonProcess = spawn('python', ['-X', 'utf8', pythonScript, dynamicValue]);

    let pythonOutput = '';
    pythonProcess.stdout.on('data', (data) => {
      console.log(data)
      pythonOutput += data.toString();
    });

    pythonProcess.stderr.on('data', (data) => {
      console.error(`Error from Python: ${data}`);
    });

    pythonProcess.on('close', async (code) => {
      console.log(`Python process exited with code ${code}`);
      const chatResponse = pythonOutput.trim();
      user.chats.push({ content: chatResponse, role: "assistant" });
      await user.save();
      return res.status(200).json({ chats: user.chats });
    });
  } catch (error) {
    console.log(error);
    return res.status(500).json({ message: "Something went wrong" });
  }
};

export const sendChatsToUser = async (
  req: Request,
  res: Response,
  next: NextFunction
) => {
  try {
    //user token check
    const user = await User.findById(res.locals.jwtData.id);
    if (!user) {
      return res.status(401).send("User not registered OR Token malfunctioned");
    }
    if (user._id.toString() !== res.locals.jwtData.id) {
      return res.status(401).send("Permissions didn't match");
    }
    return res.status(200).json({ message: "OK", chats: user.chats });
  } catch (error) {
    console.log(error);
    return res.status(200).json({ message: "ERROR", cause: error.message });
  }
};

